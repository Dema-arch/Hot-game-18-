function showScreen(screenId) {
    document.querySelectorAll('.screen').forEach(s => s.classList.remove('active'));
    document.getElementById(screenId).classList.add('active');
}

function showInstructions() {
    showScreen('instructionsScreen');
}

function startGame() {
    currentStoryIndex = 0;
    showScreen('gameScreen');
    showStory();
}

function showStory() {
    const story = stories[currentStoryIndex];
    
    document.getElementById('storyNumber').textContent = `Історія ${currentStoryIndex + 1}`;
    document.getElementById('storyTitle').textContent = story.title;
    document.getElementById('storyContent').textContent = story.content;
    
    document.getElementById('currentStory').textContent = currentStoryIndex + 1;
    document.getElementById('totalStories').textContent = stories.length;
}

function nextStory() {
    currentStoryIndex++;
    if (currentStoryIndex >= stories.length) {
        currentStoryIndex = 0;
    }
    showStory();
}

function previousStory() {
    currentStoryIndex--;
    if (currentStoryIndex < 0) {
        currentStoryIndex = stories.length - 1;
    }
    showStory();
}