// Глобальні змінні
let player1Name = '';
let player2Name = '';
let currentActionIndex = 0;
let shuffledActions = [];

function showScreen(screenId) {
    document.querySelectorAll('.screen').forEach(screen => {
        screen.classList.remove('active');
    });
    document.getElementById(screenId).classList.add('active');
}

function startGame() {
    player1Name = document.getElementById('player0').value.trim();
    player2Name = document.getElementById('player1').value.trim();
    
    if (player1Name === '' || player2Name === '') {
        alert('Будь ласка, введіть обидва імені!');
        return;
    }
    
    currentActionIndex = 0;
    shuffledActions = shuffleArray([...intimateActions]);
    
    document.getElementById('totalActions').textContent = shuffledActions.length;
    
    showScreen('gameScreen');
    displayAction();
}

function shuffleArray(array) {
    const newArray = [...array];
    for (let i = newArray.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
    }
    return newArray;
}

function displayAction() {
    if (currentActionIndex >= shuffledActions.length) {
        document.getElementById('completedActions').textContent = shuffledActions.length;
        showScreen('endScreen');
        return;
    }
    
    // Чергуємо гравців
    const currentPlayer = currentActionIndex % 2 === 0 ? player1Name : player2Name;
    document.getElementById('currentPlayer').textContent = currentPlayer;
    
    document.getElementById('actionText').textContent = shuffledActions[currentActionIndex];
    document.getElementById('currentAction').textContent = currentActionIndex + 1;
    
    const progress = ((currentActionIndex + 1) / shuffledActions.length) * 100;
    document.getElementById('progressFill').style.width = progress + '%';
}

function nextAction() {
    currentActionIndex++;
    displayAction();
}
