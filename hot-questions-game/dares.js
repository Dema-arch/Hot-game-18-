// Глобальні змінні
let player1Name = '';
let player2Name = '';
let currentDareIndex = 0;
let shuffledDares = [];

function showScreen(screenId) {
    document.querySelectorAll('.screen').forEach(screen => {
        screen.classList.remove('active');
    });
    document.getElementById(screenId).classList.add('active');
}

function startGame() {
    player1Name = document.getElementById('player0').value.trim();
    player2Name = document.getElementById('player1').value.trim();
    
    if (player1Name === '' || player2Name === '') {
        alert('Будь ласка, введіть обидва імені!');
        return;
    }
    
    currentDareIndex = 0;
    shuffledDares = shuffleArray([...coupleDares]);
    
    document.getElementById('totalDares').textContent = shuffledDares.length;
    
    showScreen('gameScreen');
    displayDare();
}

function shuffleArray(array) {
    const newArray = [...array];
    for (let i = newArray.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
    }
    return newArray;
}

function displayDare() {
    if (currentDareIndex >= shuffledDares.length) {
        showScreen('endScreen');
        return;
    }
    
    document.getElementById('dareText').textContent = shuffledDares[currentDareIndex];
    document.getElementById('currentDare').textContent = currentDareIndex + 1;
    
    const progress = ((currentDareIndex + 1) / shuffledDares.length) * 100;
    document.getElementById('progressFill').style.width = progress + '%';
}

function nextDare() {
    currentDareIndex++;
    displayDare();
}
