// Глобальні змінні
let player1Name = '';
let player2Name = '';
let currentStatementIndex = 0;
let currentPlayerIndex = 0;
let shuffledStatements = [];

function showScreen(screenId) {
    document.querySelectorAll('.screen').forEach(screen => {
        screen.classList.remove('active');
    });
    document.getElementById(screenId).classList.add('active');
}

function startGame() {
    player1Name = document.getElementById('player0').value.trim();
    player2Name = document.getElementById('player1').value.trim();
    
    if (player1Name === '' || player2Name === '') {
        alert('Будь ласка, введіть обидва імені!');
        return;
    }
    
    currentStatementIndex = 0;
    currentPlayerIndex = 0;
    
    shuffledStatements = shuffleArray([...neverStatements]);
    
    document.getElementById('totalStatements').textContent = shuffledStatements.length;
    
    showScreen('gameScreen');
    displayStatement();
}

function shuffleArray(array) {
    const newArray = [...array];
    for (let i = newArray.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
    }
    return newArray;
}

function displayStatement() {
    if (currentStatementIndex >= shuffledStatements.length) {
        showScreen('endScreen');
        return;
    }
    
    const players = [player1Name, player2Name];
    document.getElementById('currentPlayer').textContent = players[currentPlayerIndex];
    document.getElementById('statementText').textContent = shuffledStatements[currentStatementIndex];
    document.getElementById('currentStatement').textContent = currentStatementIndex + 1;
    
    const progress = ((currentStatementIndex + 1) / shuffledStatements.length) * 100;
    document.getElementById('progressFill').style.width = progress + '%';
}

function nextStatement() {
    currentStatementIndex++;
    currentPlayerIndex = (currentPlayerIndex + 1) % 2;
    displayStatement();
}
