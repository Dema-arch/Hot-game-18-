// Глобальні змінні
let currentLevel = '';
let players = [];
let currentPlayerIndex = 0;
let truths = [];
let dares = [];

function showScreen(screenId) {
    document.querySelectorAll('.screen').forEach(screen => {
        screen.classList.remove('active');
    });
    document.getElementById(screenId).classList.add('active');
}

function selectLevel(level) {
    currentLevel = level;
    
    if (level === 'easy') {
        truths = easyTruths;
        dares = easyDares;
    } else if (level === 'normal') {
        truths = normalTruths;
        dares = normalDares;
    } else {
        truths = crazyTruths;
        dares = crazyDares;
    }
    
    showScreen('nameScreen');
}

function startGame() {
    players = [];
    for (let i = 0; i < 4; i++) {
        const name = document.getElementById(`player${i}`).value.trim();
        if (name !== '') {
            players.push(name);
        }
    }
    
    if (players.length < 3) {
        alert('Потрібно мінімум 3 гравці!');
        return;
    }
    
    currentPlayerIndex = 0;
    showScreen('gameScreen');
    displayChoice();
}

function displayChoice() {
    document.getElementById('currentPlayer').textContent = players[currentPlayerIndex];
    document.getElementById('todCard').classList.add('hidden');
}

function chooseTruth() {
    const randomTruth = truths[Math.floor(Math.random() * truths.length)];
    document.getElementById('todText').textContent = randomTruth;
    document.getElementById('todCard').classList.remove('hidden');
}

function chooseDare() {
    const randomDare = dares[Math.floor(Math.random() * dares.length)];
    document.getElementById('todText').textContent = randomDare;
    document.getElementById('todCard').classList.remove('hidden');
}

function nextTurn() {
    currentPlayerIndex = (currentPlayerIndex + 1) % players.length;
    displayChoice();
}
