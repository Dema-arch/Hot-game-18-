// Глобальні змінні
let players = [];
let currentQuestionIndex = 0;
let currentPlayerIndex = 0;
let shuffledQuestions = [];

function showScreen(screenId) {
    document.querySelectorAll('.screen').forEach(screen => {
        screen.classList.remove('active');
    });
    document.getElementById(screenId).classList.add('active');
}

function startGame() {
    players = [];
    for (let i = 0; i < 4; i++) {
        const name = document.getElementById(`player${i}`).value.trim();
        if (name === '') {
            alert('Будь ласка, введіть всі імена!');
            return;
        }
        players.push(name);
    }
    
    currentQuestionIndex = 0;
    currentPlayerIndex = 0;
    
    shuffledQuestions = shuffleArray([...partyQuestions]);
    
    document.getElementById('totalQuestions').textContent = shuffledQuestions.length;
    
    showScreen('gameScreen');
    displayQuestion();
}

function shuffleArray(array) {
    const newArray = [...array];
    for (let i = newArray.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
    }
    return newArray;
}

function displayQuestion() {
    if (currentQuestionIndex >= shuffledQuestions.length) {
        showScreen('endScreen');
        return;
    }
    
    document.getElementById('currentPlayer').textContent = players[currentPlayerIndex];
    document.getElementById('questionText').textContent = shuffledQuestions[currentQuestionIndex];
    document.getElementById('currentQuestion').textContent = currentQuestionIndex + 1;
    
    const progress = ((currentQuestionIndex + 1) / shuffledQuestions.length) * 100;
    document.getElementById('progressFill').style.width = progress + '%';
}

function nextQuestion() {
    currentQuestionIndex++;
    currentPlayerIndex = (currentPlayerIndex + 1) % players.length;
    displayQuestion();
}
