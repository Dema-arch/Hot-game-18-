// Глобальні змінні
let player1Name = '';
let player2Name = '';
let currentRound = 0;
let currentPlayerIndex = 0;

function showScreen(screenId) {
    document.querySelectorAll('.screen').forEach(screen => {
        screen.classList.remove('active');
    });
    document.getElementById(screenId).classList.add('active');
}

function startGame() {
    player1Name = document.getElementById('player0').value.trim();
    player2Name = document.getElementById('player1').value.trim();
    
    if (player1Name === '' || player2Name === '') {
        alert('Будь ласка, введіть обидва імені!');
        return;
    }
    
    currentRound = 0;
    currentPlayerIndex = 0;
    
    showScreen('gameScreen');
    displayChoice();
}

function displayChoice() {
    const players = [player1Name, player2Name];
    document.getElementById('currentPlayer').textContent = players[currentPlayerIndex];
    document.getElementById('todCard').classList.add('hidden');
}

function chooseTruth() {
    const randomQuestion = truthQuestions[Math.floor(Math.random() * truthQuestions.length)];
    document.getElementById('todText').textContent = randomQuestion;
    document.getElementById('todCard').classList.remove('hidden');
}

function chooseDare() {
    const randomDare = dareActions[Math.floor(Math.random() * dareActions.length)];
    document.getElementById('todText').textContent = randomDare;
    document.getElementById('todCard').classList.remove('hidden');
}

function nextTurn() {
    currentRound++;
    currentPlayerIndex = (currentPlayerIndex + 1) % 2;
    displayChoice();
}
