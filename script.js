function selectQuestionsMode(mode) {
    gameMode = mode;
    showScreen('playerCountScreen');
}

function selectPlayerCount(count) {
    playerCount = count;
    createNameInputs();
    showScreen('nameScreen');
}

function createNameInputs() {
    const container = document.getElementById('nameInputs');
    container.innerHTML = '';
    
    for (let i = 0; i < playerCount; i++) {
        const input = document.createElement('input');
        input.type = 'text';
        input.className = 'name-input';
        input.placeholder = Гравець ;
        input.id = player;
        container.appendChild(input);
    }
}

function startCurrentGame() {
    players = [];
    for (let i = 0; i < playerCount; i++) {
        const name = document.getElementById(player).value.trim();
        if (name === '') {
            alert('Будь ласка, введіть всі імена!');
            return;
        }
        players.push(name);
    }
    
    questions = gameMode === 'couple' ? [...coupleQuestions] : [...partyQuestions];
    questions = shuffleArray(questions);
    
    currentQuestionIndex = 0;
    currentPlayerIndex = 0;
    
    document.getElementById('totalQuestions').textContent = questions.length;
    
    showScreen('gameScreen');
    displayQuestion();
}

function shuffleArray(array) {
    const newArray = [...array];
    for (let i = newArray.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
    }
    return newArray;
}

function displayQuestion() {
    if (currentQuestionIndex >= questions.length) {
        showScreen('endScreen');
        return;
    }
    
    document.getElementById('currentPlayer').textContent = players[currentPlayerIndex];
    document.getElementById('questionText').textContent = questions[currentQuestionIndex];
    document.getElementById('currentQuestion').textContent = currentQuestionIndex + 1;
    
    const progress = ((currentQuestionIndex + 1) / questions.length) * 100;
    document.getElementById('progressFill').style.width = progress + '%';
}

function nextQuestion() {
    currentQuestionIndex++;
    currentPlayerIndex = (currentPlayerIndex + 1) % playerCount;
    displayQuestion();
}

function goBackInGame() {
    if (currentGame === 'questions') {
        showScreen('questionsMode');
    } else {
        showScreen('playerCountScreen');
    }
}

