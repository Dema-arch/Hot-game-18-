// Вибір гри
function selectGame(game) {
    currentGame = game;
    
    if (game === 'questions') {
        showScreen('questionsMode');
    } else if (game === 'truthOrDare') {
        setupTruthOrDare();
    } else if (game === 'actions') {
        setupActions();
    } else if (game === 'dares') {
        setupDares();
    } else if (game === 'spinBottle') {
        setupSpinBottle();
    } else if (game === 'neverHaveI') {
        setupNeverHaveI();
    }
}

