function selectLevel(level) {
    currentLevel = level;
    currentPositionIndex = 0;
    showScreen('gameScreen');
    updateLevelIndicator();
    showPosition();
}

function showScreen(screenId) {
    document.querySelectorAll('.screen').forEach(s => s.classList.remove('active'));
    document.getElementById(screenId).classList.add('active');
}

function updateLevelIndicator() {
    const indicator = document.getElementById('levelIndicator');
    const levels = {
        basic: { emoji: '😊', name: 'Базові позиції' },
        medium: { emoji: '🔥', name: 'Середні позиції' },
        extreme: { emoji: '💋', name: 'Екстремальні позиції' }
    };
    indicator.textContent = `${levels[currentLevel].emoji} ${levels[currentLevel].name}`;
}

function toggleTimer() {
    timerEnabled = document.getElementById('timerEnabled').checked;
}

function showPosition() {
    const position = positions[currentLevel][currentPositionIndex];
    const total = positions[currentLevel].length;
    
    document.getElementById('positionNumber').textContent = `Позиція ${currentPositionIndex + 1}`;
    document.getElementById('positionName').textContent = position.name;
    document.getElementById('positionDescription').textContent = position.description;
    document.getElementById('positionInstructions').textContent = position.instructions;
    
    document.getElementById('currentPosition').textContent = currentPositionIndex + 1;
    document.getElementById('totalPositions').textContent = total;
    
    if (timerEnabled) {
        startTimer();
    }
}

function startTimer() {
    stopTimer();
    timeLeft = 120;
    document.getElementById('timerDisplay').style.display = 'block';
    updateTimerDisplay();
    
    timerInterval = setInterval(() => {
        timeLeft--;
        updateTimerDisplay();
        
        if (timeLeft === 0) {
            stopTimer();
            alert('Час вийшов! Переходьте до наступної позиції.');
        }
    }, 1000);
}

function stopTimer() {
    if (timerInterval) {
        clearInterval(timerInterval);
        timerInterval = null;
    }
}

function updateTimerDisplay() {
    const minutes = Math.floor(timeLeft / 60);
    const seconds = timeLeft % 60;
    document.getElementById('timerValue').textContent = 
        `${minutes}:${seconds.toString().padStart(2, '0')}`;
}

function nextPosition() {
    stopTimer();
    currentPositionIndex++;
    if (currentPositionIndex >= positions[currentLevel].length) {
        currentPositionIndex = 0;
    }
    showPosition();
}

function previousPosition() {
    stopTimer();
    currentPositionIndex--;
    if (currentPositionIndex < 0) {
        currentPositionIndex = positions[currentLevel].length - 1;
    }
    showPosition();
}

function resetGame() {
    stopTimer();
    currentPositionIndex = 0;
    document.getElementById('timerDisplay').style.display = 'none';
    showScreen('levelScreen');
}